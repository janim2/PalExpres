package com.test.palexpress.Models;

public class FoodModel {
    String food_id;
    String food_image;
    String food_name;
    String food_description;
    String food_prize;

    public void setFood_id(String food_id){this.food_id = food_id;}
    public String getFood_id(){return food_id;}

    public void setFood_image(String food_image){this.food_image = food_image;}
    public String getFood_image(){return food_image;}

    public void setFood_name(String food_name){this.food_name = food_name;}
    public String getFood_name(){return food_name;}

    public void setFood_description(String food_description){this.food_description = food_description;}
    public String getFood_description(){return food_description;}

    public void setFood_prize(String food_prize){this.food_prize = food_prize;}
    public String getFood_prize(){return food_prize;}

}
